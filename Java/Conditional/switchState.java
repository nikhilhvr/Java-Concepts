public class switchState {

        public static void main(String[] args) {

        int cars = 1;

        switch (cars) {
            case 1:
                System.out.println("Ferrari");

                break;
            case 2:
                System.out.println("Supra");

                break;

            default:
                System.out.println("Mustang");
                break;
        }
    }
}
