public class logicalOpetrators {
    
    public static void main(String[] args) {
        
        int numOne = 9;
        int numTwo =2;
        System.out.println((numOne>numTwo) && (numOne<numTwo));
        System.out.println((numOne>numTwo) || (numOne<numTwo));
        System.out.println(!(numOne>numTwo));
    }
}
