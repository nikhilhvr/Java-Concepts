public class assignmentOperators {
 
    public static void main(String[] args) {
  
        int numOne = 21;
        
        System.out.println(numOne=5);
        System.out.println(numOne+=5);
        System.out.println(numOne-=5);
        System.out.println(numOne*=5);
        System.out.println(numOne/=5);
        
        
    }
}
