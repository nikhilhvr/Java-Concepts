public class relationalOperators {

    public static void main(String[] args) {
        
        int numOne =21;
        int numTwo =31;

        System.out.println( "equalTO ="+(numOne==numTwo));
        System.out.println( "notEqualTO =" +(numOne!=numTwo));
        System.out.println( "lessThan =" +(numOne<numTwo));
        System.out.println( "greaterThan =" +(numOne>numTwo));
        System.out.println( "lessThanEqualTo =" +(numOne<=numTwo));
        System.out.println( "greaterThanEqualTo =" +(numOne>=numTwo));
    }
    
}
